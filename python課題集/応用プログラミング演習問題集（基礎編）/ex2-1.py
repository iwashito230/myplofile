''' 「（33+44）×55」と「2.1+4.3+6.5」と「123+456を78で割った余り」を計算し，表示するプログラムを作ってみましょう．
    その際に，（33+44）*55＝のように計算式も表示しましょう．

'''

def main():
    equation = ['(33+44)*55',  '2.1+4.3+6.5', '(123+456)%78']
    anther = [(33+44)*55,  2.1+4.3+6.5, (123+456)%78]

    for (e, a) in zip(equation, anther):
        print(f'{e} = {a}')


if __name__ == "__main__":
    main()
